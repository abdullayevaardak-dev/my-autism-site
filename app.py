# --- БАЗА ДАННЫХ ---
def init_db():
    conn = sqlite3.connect('school_consilium_v10.db')
    c = conn.cursor()
    c.execute('''CREATE TABLE IF NOT EXISTS teacher_reports 
                 (id INTEGER PRIMARY KEY AUTOINCREMENT,
                  date TEXT, 
                  teacher_name TEXT, 
                  child_id TEXT, 
                  asd_score INTEGER,
                  adhd_score INTEGER,
                  details TEXT,
                  adhd_details TEXT,
                  asd_notes TEXT,
                  adhd_notes TEXT,
                  lang TEXT)''')
    conn.commit()
    conn.close()

init_db()

