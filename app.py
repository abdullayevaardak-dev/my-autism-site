import streamlit as st

# Настройка вкладки браузера
st.set_page_config(page_title="Спектр-Помощь", page_icon="🧩")

# Заголовок
st.title("🧩 Система «Спектр-Помощь»")
st.write("Первичный скрининг признаков РАС у учеников 1-2 классов.")

# Боковое меню
with st.sidebar:
    st.header("Настройки")
    child_id = st.text_input("Код ученика", "Ученик-01")
    st.divider()
    st.info("Это вспомогательный инструмент для учителя.")

# Создание вкладок
tab1, tab2, tab3 = st.tabs(["📋 Анкета учителя", "🎨 Тест ребенка", "📖 Справка"])

with tab1:
    st.subheader("Наблюдения педагога")
    q1 = st.checkbox("Ребенок избегает зрительного контакта")
    q2 = st.checkbox("Трудности в общении со сверстниками")
    q3 = st.checkbox("Необычные повторяющиеся движения")
    q4 = st.checkbox("Острая реакция на шум или свет")
    
    if st.button("Проверить результат"):
        score = sum([q1, q2, q3, q4])
        if score >= 2:
            st.warning(f"Выявлено признаков: {score}. Рекомендуется консультация психолога.")
        else:
            st.success("Явных маркеров не обнаружено.")

with tab2:
    st.subheader("Интерактив с ребенком")
    ans = st.radio("Как ты любишь играть с машинками/куклами?", 
                   ["Выстраиваю в ряд", "Придумываю историю", "Просто ношу с собой"])
    if st.button("Сохранить ответ"):
        st.balloons()
        st.success("Ответ записан.")

with tab3:
    st.subheader("Памятка для учителя")
    st.write("Если вы заметили особенности, не ставьте диагноз самостоятельно.")
    st.write("Ваша задача — зафиксировать факты для передачи психологу.")
